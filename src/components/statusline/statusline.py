_ANSI_RE: re.Pattern[str] = re.compile(r'\033(?:\[[^m]*m|\][^\a]*\a)')


def _visual_len(s: str) -> int:
    """String length excluding ANSI escape sequences."""
    return len(_ANSI_RE.sub('', s))


